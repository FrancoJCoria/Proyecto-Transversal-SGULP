     private void cargarAlumnos() {
        try {
            
            List<Alumno> alumnos = aData.listarAlumnos();
            BoxAlumno.removeAllItems();
            
            for (Alumno a : alumnos) {
                BoxAlumno.addItem(a);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar alumnos: " + e.getMessage());
        }

    }


